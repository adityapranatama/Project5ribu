              <h4>Tugas Pengamatan Laut</h4>
              <ol><h6>
                <li>Mengumpulkan data sebagai bahan pemutakhiran data pada buku petunjuk pelayaran dan peta laut.</li>
                <li>Melaksanakan survei kenavigasian guna penetapan alur pelayaran, posisi dan jenis sarana bantu navigasi pelayaran, skema pemisah lalu lintas di laut, rute dan arah, garis haluan yang dianjurkan, rute air dalam, daerah yang harus dihindari, daerah lalu lintas pedalaman dan daerah kewaspadaan serta pembangunan sarana bantu navigasi pelayaran.</li>
              </h6></ol><br>

              <h6>Pengolahan dan Pengumpulan Data</h6>
              <ol><h6>
                <li>Penentuan posisi.</li>
                <li>Pengukuran kedalam perairan.</li>
                <li>Pengamatan pasang surut.</li>
                <li>Pengamatan arus.</li>
                <li>Pengamatan kadar garam.</li>
                <li>Pengamatan tinggi rendahnya gelombang.</li>
                <li>Pengambilan jenis bahan dasar laut.</li>
                <li>Pengukuran elevasi sarana bantu navigasi pelayaran.</li>
                <li>Pengukuran jarak tampak sarana bantu navigasi pelayaran.</li>
              </h6></ol><br>

